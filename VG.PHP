<p> पालेभाज्या आणि त्याचे फायदे  
- पालक :  पचनसंस्था आणि मूत्रसंस्था यांच्या आतील सूज कमी करुन मऊपणा आणण्यास उपयुक्त आहे. दमा आणि खोकला कमी करणारी ही भाजी आहे. 

-  मेथी :  सारक आणि पथ्यकर असलेल्या या भाजीमध्ये लोहाचे प्रमाण जास्त असते. याचे बी वातहारक व शक्तिकारक असते म्हणून बाळंतपणात याचा उपयोग केला जातो. दूधवाढीसाठीही त्याचा उपयोग होतो. मेथ्यांचे पीठ तोंडाला लावल्यास चेहर्‍यावरील सुरकुत्या नष्ट होतात व चेहरा तजेलदार दिसतो. - शेवगा :  ही भाजी वातनाशक आणि पित्तनाशक आहे. हृदय आणि रुधिराभिसरण क्रिया यामुळे सुधारण्यास मदत होते.

-  अळू  :  याच्या पानांचा आणि दांड्यांचा रस जंतुनाशक आहे. त्यामुळे शरिराच्या कापलेल्या भागावर याचा रस लावून पट्टी बांधल्यास जखम लवकर भरून येते. -  टाकळा : सर्वांगाला येणारी खाज ही भाजी खाल्याने कमी होते. या भाजीच्या बियांचे पीठ अशक्तपाणा कमी करण्यास मदत करते.

- कोथिंबीर :  उष्णता कमी करणारी, पित्तनाशक आहे. हृदरोगावर अतिशय उपयुक्त. - कडूलिंब  :  पित्तनाशक आणि कृमीनाशक म्हणून याचा उपयोग 

-  शेपू :  वातनाशक आणि पोटदुखी कमी करणारी अशी ही भाजी आहे.  

- माठ  :  हृदय चांगले ठेवण्यास मदत करणारी आणि ही भाजी खाल्यावर शौचाला साफ होते. 

- अंबाडी :  मीरपूड आणि साखर यांच्याबरोबर अंबाडीचा रस सेवन केल्यास तो पित्त कमी होते.

-  तांदूळजा : बाळंतीणीला दूध वाढवण्यास ही भाजी उपयोगी असते.

- चाकवत : ही भाजी पचनास चांगली असून ज्वरनाशक आहे. त्वचेला कांती देणारी  भाजी आहे. ही भाजी कृमीनाशक असून यकृताच्या सर्व विकारांवर उपयुक्त ठरते.

- हादगा : पित्त, हिवताप, खोकला कमी करणारी भाजी. या भाजीच्या फुलाच्या रसाचे दोन थेंब काही दिवस डोळ्यात घातल्यास रातआंधळेपणा कमी होण्यास मदत होतो, असे सांगितले जाते. हा फुलांचा रस मधातून घेतल्याने छातीतील कफ पातळ होतो.  

- घोळ :  मूळव्याध कमी करण्यास ही भाजी उपयुक्त. तसेच लघवीला साफ होते. 
 
-  मायाळू : अंगावर पित्त उठले असताना याच्या पानांचा रस आंगाला चोळल्यास पित्त कमी होते. लहानमुलांना थंडी खोकला झाल्यास त्यांना मायाळूच्या पानाचा रस चमचाभर द्या. तो कमी होतो. </p>